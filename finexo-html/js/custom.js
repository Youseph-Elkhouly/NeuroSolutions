$(document).ready(function() {
  $("a[href^='#']").on('click', function(event) {
    var target = this.hash;
    var $target = $(target);
    $('html, body').animate({
      scrollTop: $target.offset().top - 150 // Adjust the offset value to scroll less down
    }, 1000, 'swing');
  });
});


